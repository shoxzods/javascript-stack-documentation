//------------------- INSTALLING TYPESCRIPT ( GLOBAL ,  COMPILER ): ------------------------------------------------------------------//

// global installing ( устанваливает версию везде )
/* 
    npm install (i) -g typescript ( typescript@version ):

    дает возможность не использовать команду tsc 
*/


// local installing ( установка только на файлах проекта ) 
/*
    npm install (i) typescript --save dev ( typescript@version ):

    используется только с tsc
*/

// versions:
/*
    npx tsc -v ( local )
    tsc -v ( global )
*/
