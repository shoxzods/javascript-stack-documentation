/*
 npx tsc fileName.ts - компилирует typescript код на js
*/

// установка ts-node:
/*
    ts-node помогает запустить ts файлы без компиляции в js.
    установка ts-node: npm  install -g ts-node
    запуск ts-node: npx ts-node file.ts

    ts-node не будет работать стабильно без конфигурации( ts.config.json )
*/